// database/generate-pg-schema.js — SQLite → PostgreSQL schema translator
//
// Run: node database/generate-pg-schema.js
// Output: database/pg-schema.sql
//
// This reads the CURRENT LIVE schema directly from qsl_erp.db (the actual
// source of truth) and mechanically translates it, rather than maintaining
// a hand-written copy that drifts out of sync every time a table changes.
// The previous migrate-postgres.js was exactly this kind of drift — it
// hard-coded 21 tables from an early point in development and was never
// updated, while the live schema grew to 80 tables. Re-running this
// generator after any schema change keeps the Postgres DDL honest.
//
// Translation rules (verified against the actual schema, not assumed):
//   - TEXT PRIMARY KEY              -> TEXT PRIMARY KEY (unchanged — every
//     table here uses app-generated UUIDs via the `uuid` package, never
//     SQLite AUTOINCREMENT/ROWID, so no IDENTITY/SERIAL conversion needed)
//   - INTEGER DEFAULT 0/1           -> INTEGER DEFAULT 0/1 (unchanged —
//     used as booleans throughout the app code, e.g. `enabled=1`; keeping
//     them as INTEGER avoids having to touch every route's comparisons)
//   - datetime('now')               -> CURRENT_TIMESTAMP
//   - date('now')                   -> CURRENT_DATE
//   - REAL                          -> NUMERIC(15,2) (money/quantity columns
//     — floating point REAL is the wrong type for financial data even in
//     SQLite; this is a deliberate correctness improvement, not just a
//     syntax port)
//   - TEXT REFERENCES x(id)         -> TEXT REFERENCES x(id) (unchanged)
//   - UNIQUE(a, b)                  -> UNIQUE(a, b) (unchanged, already
//     valid Postgres syntax)
//   - CREATE INDEX ...              -> unchanged (already valid Postgres
//     syntax, confirmed by inspecting all 12 existing indexes)

const path = require('path');
const fs = require('fs');

async function main() {
  const db = require('../src/lib/db.js');

  const tables = await db.query(`SELECT name, sql FROM sqlite_master WHERE type='table' AND sql IS NOT NULL ORDER BY name`);
  const indexes = await db.query(`SELECT sql FROM sqlite_master WHERE type='index' AND sql IS NOT NULL ORDER BY name`);

  console.log(`Read ${tables.length} tables and ${indexes.length} indexes from the live database.`);

  function translateTable(sql) {
    let t = sql;

    // datetime('now') / date('now') -> Postgres equivalents
    t = t.replace(/datetime\('now'\)/g, 'CURRENT_TIMESTAMP');
    t = t.replace(/date\('now'\)/g, 'CURRENT_DATE');

    // REAL -> NUMERIC(15,2). Every REAL column in this schema is money,
    // a quantity, or a percentage/rate — none are scientific/float data —
    // confirmed by inspecting the full table list before writing this.
    t = t.replace(/\bREAL\b/g, 'NUMERIC(15,2)');

    return t;
  }

  const pgStatements = tables.map(t => translateTable(t.sql) + ';');
  const pgIndexes = indexes.map(i => i.sql + ';');

  const header = `-- database/pg-schema.sql — Generated PostgreSQL schema
-- AUTO-GENERATED by database/generate-pg-schema.js — do not hand-edit.
-- Regenerate after any schema change in database/init.js or any migrate-*.js
-- by running: node database/generate-pg-schema.js
-- Generated: ${new Date().toISOString()}
-- Source: live qsl_erp.db (${tables.length} tables, ${indexes.length} indexes)

CREATE EXTENSION IF NOT EXISTS "pgcrypto";

`;

  const out = header + pgStatements.join('\n\n') + '\n\n-- ── INDEXES ──────────────────────────────────────────────\n\n' + pgIndexes.join('\n');

  const outPath = path.join(__dirname, 'pg-schema.sql');
  fs.writeFileSync(outPath, out);
  console.log(`Wrote ${outPath} (${tables.length} tables, ${indexes.length} indexes)`);
}

main().catch(e => { console.error(e); process.exit(1); });
